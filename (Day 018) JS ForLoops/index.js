// For loop = mengulang bebeapa kode dengan jumlah waktu terbatas

for(let i = 0; i <= 2; i++) { // mengulang loop sebanyak 3x ( 0, 1 ,2 )
    console.log(i); // Tampilkan output i
}

// penjelasan
// i = variabel counter
// let i = 0, adalah posisi default value atau nilai awal variabel i (0 dalam koding adalah angka 1)
// jika i kurang dari sama dengan (<=) 2,
// perintahkan variable i untuk membuat increment sehingga variabel i nantinya mendapatkan value / nilai 2
// (++) adalah increment, fungsinya adalah menambahkan nilai variabel sejumlah 1.
// (+=2) adalah increment, bedanya dia menambahkan nilai variabel sejumlah 2.


